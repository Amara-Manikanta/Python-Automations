
import glob
import shutil
import os

files=['.pptx','.pdf','.rtf','.docx','.zip','.mp3','.cab','.jar','.iso','.ppt','.png','.MPG','.mp4','.dng','.3gp']
files_paths=[]
for j in files:
    ############################################################################################################################################################################
        #collecting user specified .extension files
    ############################################################################################################################################################################
    local_files=glob.glob(r"I:/*"+j)
    files_paths.extend(local_files)
    ############################################################################################################################################################################
    #once files are collected , we will create a directory with that extension name
    ############################################################################################################################################################################
    os.mkdir('I:\\'+str(j[1:]))
    for i in files_paths:
        ############################################################################################################################################################################
            #Moving the files to the destination folder
        ############################################################################################################################################################################
        destination = 'I:\\'+str(j[1:])
        shutil.move(i, destination)









